// Declaration & assign
const pi = 3.14; // `const` untuk menyimpan nilai tetap
let name; // `let` untuk menyimpan nilai yang berubah-ubah
name = "Rommy";
name = "Rommy Gustiawan";

// Primitive data type
const number = 56.78; // hanya ada satu tipe untuk angka yaitu `number`
const string = "Hello, World!";
const boolean = false;
let undefined; // bernilai `undefined` jika hanya di-declare tapi tidak di-assign

// Printing
console.log(`Name: ${name}`);
console.log(`Numbers: ${number}`);
console.log(`Strings: ${string}`);
console.log(`Boolean: ${boolean}`);
console.log(`Undefined: ${typeof undefined}`); // `typeof` untuk mengecek tipe sebuah variable

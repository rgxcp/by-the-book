// Assignment Operators
let message = "Hello, ";
message += "World!"; // Artinya `message = message + "World!"`, berlaku untuk arithmetic operators lainnya
console.log(`Message: ${message}`);

// Comparison Operators
console.log(`5 == "5" adalah ${5 == "5"}`);
console.log(`5 === "5" adalah ${5 === "5"}`); // `===` untuk mengecek value sekaligus tipe-nya
